package com.LSK.iamlivingalone;

public class Memo {
    public String Title;
    public String Content;
    public String uid;

    @Override
    public String toString() {
        return "Memo{" +
                "Title='" + Title + '\'' +
                ", Content='" + Content + '\'' +
                ", uid='" + uid + '\'' +
                '}';
    }
}
